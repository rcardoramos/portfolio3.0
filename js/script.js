let currentLang = 'es';

function updateText(lang) {
    const t = translations[lang];

    const updateElement = (selector, content, isPlaceholder = false) => {
        const el = document.querySelector(selector);
        if (el) {
            if (isPlaceholder) {
                el.placeholder = content;
            } else if (selector.includes('_html')) {
                el.innerHTML = content;
            } else {
                el.textContent = content;
            }
        }
    };

    const updateElementHTML = (selector, content) => {
        const el = document.querySelector(selector);
        if (el) {
            el.innerHTML = content;
        }
    };

    updateElement('#page-title', t.page_title);

    document.querySelectorAll('.nav-link').forEach(el => {
        const key = el.getAttribute('data-key');
        if (el) el.textContent = t[key];
    });

    const cvButton = document.querySelector('.cv-button-compact');
    if (cvButton) cvButton.textContent = t.nav_cv;

    // Update Hero Section
    updateElement('[data-key="hero_greeting"]', t.hero_greeting);
    updateElement('[data-key="hero_title"]', t.hero_title);

    // Update Section Headings
    updateElement('[data-key="section_about"]', t.section_about);
    updateElement('[data-key="section_skills"]', t.section_skills);
    updateElement('[data-key="section_experience"]', t.section_experience);
    updateElement('[data-key="section_projects"]', t.section_projects);
    updateElement('[data-key="section_contact_heading"]', t.section_contact_heading);

    // Update Skills Subtitle
    updateElement('[data-key="skills_subtitle"]', t.skills_subtitle);

    // Update About Me 
    updateElementHTML('[data-key="about_p1"]', t.about_p1);
    updateElementHTML('[data-key="about_p2"]', t.about_p2);
    updateElementHTML('[data-key="about_p3"]', t.about_p3);
    updateElementHTML('[data-key="about_p4"]', t.about_p4);
    updateElementHTML('[data-key="about_quote"]', t.about_quote);

    // Update Project Details
    updateElement('[data-key="proj1_category"]', t.proj1_category);
    updateElement('[data-key="proj1_title_pre"]', t.proj1_title_pre);
    updateElement('[data-key="proj1_title_post"]', t.proj1_title_post);
    updateElement('[data-key="proj1_desc"]', t.proj1_desc);

    updateElement('[data-key="proj2_category"]', t.proj2_category);
    updateElement('[data-key="proj2_title_pre"]', t.proj2_title_pre);
    updateElement('[data-key="proj2_title_post"]', t.proj2_title_post);
    updateElement('[data-key="proj2_desc"]', t.proj2_desc);

    updateElement('[data-key="proj3_category"]', t.proj3_category);
    updateElement('[data-key="proj3_title_pre"]', t.proj3_title_pre);
    updateElement('[data-key="proj3_title_post"]', t.proj3_title_post);
    updateElement('[data-key="proj3_desc"]', t.proj3_desc);

    updateElement('[data-key="btn_view_repos"]', t.btn_view_repos);


    // Update Tech Stack Cards
    updateElement('[data-key="tech_group_frontend"]', t.tech_group_frontend);
    updateElement('[data-key="desc_high_performance"]', t.desc_high_performance);
    updateElement('[data-key="desc_robust_spas"]', t.desc_robust_spas);
    updateElement('[data-key="desc_modern_base"]', t.desc_modern_base);
    updateElement('[data-key="desc_ui_ux"]', t.desc_ui_ux);

    updateElement('[data-key="tech_group_backend_db"]', t.tech_group_backend_db);
    updateElement('[data-key="desc_apis_concurrency"]', t.desc_apis_concurrency);
    updateElement('[data-key="desc_nosql_systems"]', t.desc_nosql_systems);
    updateElement('[data-key="desc_sql_systems"]', t.desc_sql_systems);
    updateElement('[data-key="desc_data_ml"]', t.desc_data_ml);

    updateElement('[data-key="tech_group_tools_practices"]', t.tech_group_tools_practices);
    updateElement('[data-key="desc_version_control"]', t.desc_version_control);
    updateElement('[data-key="desc_agile_management"]', t.desc_agile_management);
    updateElement('[data-key="desc_ux_expertise"]', t.desc_ux_expertise);
    updateElement('[data-key="desc_hosting"]', t.desc_hosting);


    // Update Contact Form
    updateElement('[data-key="contact_greeting"]', t.contact_greeting);

    updateElement('[data-key="label_name"]', t.label_name);
    updateElement('[data-key="placeholder_name"]', t.placeholder_name, true);

    updateElement('[data-key="label_email"]', t.label_email);
    updateElement('[data-key="placeholder_email"]', t.placeholder_email, true);

    updateElement('[data-key="label_message_optional"]', t.label_message_optional);
    updateElement('[data-key="placeholder_message"]', t.placeholder_message, true);

    const btnSend = document.querySelector('[data-key="btn_send_code"]');
    if (btnSend) btnSend.innerHTML = t.btn_send_code + ' <i data-lucide="send" class="w-5 h-5 ml-2"></i>';

    updateElementHTML('[data-key="contact_highlight_1"]', t.contact_highlight_1);
    updateElementHTML('[data-key="contact_highlight_2"]', t.contact_highlight_2.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>'));


    document.getElementById('footer-credits-text').innerHTML = t.footer_credits;

    if (document.getElementById('job-tabs')) {
        document.getElementById('job-tabs').innerHTML = '';
        initJobTabs();
    }

    document.querySelectorAll('.lang-selector span').forEach(span => {
        span.classList.remove('active');
        if (span.getAttribute('data-lang') === lang) {
            span.classList.add('active');
        }
    });

    currentLang = lang;
    lucide.createIcons();
}

function switchLanguage(event) {
    const lang = event.target.getAttribute('data-lang');
    if (lang && lang !== currentLang) {
        updateText(lang);
    }
}

document.addEventListener('DOMContentLoaded', () => {
    document.getElementById('lang-es').addEventListener('click', switchLanguage);
    document.getElementById('lang-en').addEventListener('click', switchLanguage);
});






